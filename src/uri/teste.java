import java.util.*;

public class teste {

    public static void main(String[] args) {
        Scanner teclado = new Scanner(System.in);
        int n = teclado.nextInt();
        for (int i = n; i <= n; i++) {
            for (int j = 1; j <= i; j ++){
                System.out.printf("* "); 
            }
            System.out.printf("\n");
        }
    }
}